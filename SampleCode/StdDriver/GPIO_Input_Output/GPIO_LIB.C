#include "MUG51.h"


//----------------------------------------------------------------------------------------------//
void main (void)
{
/* As defaut all multi function define as GPIO */ 
    GPIO_SetMode(Port1,BIT0|BIT5|BIT7,GPIO_MODE_QUASI);
    GPIO_SetMode(Port4,BIT0|BIT1,GPIO_MODE_PUSHPULL);
  while(1)
  {
    P1 = ~P1;
    P4 = ~P4;
    Timer2_Delay100ms(2);
  }
}



